// for loop----
// Initialization: Runs once before the loop starts to set up a counter variable.
// .Condition: Evaluated before every iteration. If true, the code block runs; if false, the loop stops.
// Afterthought: Executes at the end of each iteration loop (usually to increment or decrement the counter).

for (let i = 0; i < 3; i++) {
  console.log(i); //0,1,2
}

// while statement ---
let count = 0;
while (count < 3) {
  console.log(count);
  count++; 
}
