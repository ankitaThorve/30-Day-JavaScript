// Arithmatic Operators
let add = 4 + 5; // addition
console.log(add);

let substract = 4 - 5; // substraction
console.log(substract);

let multiply = 4 * 5; // multiplication
console.log(multiply);

let divide = 4 / 5; // division
console.log(divide);

let remainder = 4 % 5; // remainder / modulus
console.log(remainder);

let exponentiation = 4 ** 5;
console.log(exponentiation);

// Assignment Operators

let x = 1; // assignment
let y = 2;

x += y;
console.log(x); // addition assignment x = x + y

x -= y;
console.log(x); //substraction assignment

x *= y;
console.log(x); //multiplication assignment

x /= y;
console.log(x); //division assignment

// Comparison / Relational Operators

console.log(1 == 1.2); // loose equality

console.log(1 === 1.2); // strict equality

console.log(1 != 1.2); // loose inequality

console.log(1 !== 1.2); // strict inequality

console.log(1 > 1.2); // greater than

console.log(1 < 1.2); // less than

console.log(1 >= 1.2); // greater than or equal to

console.log(1 <= 1.2); // less than or equal to

